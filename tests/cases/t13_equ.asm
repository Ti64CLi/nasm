 MOV r0,#COLOR
 CMP r0,#COLOR
 ADD r1,r2,#CHAR_A
 SUB r3,r4,#CHAR_A+1
 MOV r5,r6,LSL #SHIFT
 LDR r7,[r8,#OFF]
 STR r7,[r8],#OFF
 LDRH r9,[r10,#HOFF]
 LDR r9,=BIGCONST
 LDR r11,=DERIVED
 SWI SWINUM
 SWIEQ #SWINUM
 MSR CPSR_FLG,#FLAGS
 DCD COLOR,DERIVED
 DCW COLOR
 DCB NEG+5,CHAR_A,CHAR_A-32
