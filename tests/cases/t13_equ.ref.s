.equ COLOR, 0xF800
.equ NEG, -4
.equ CHAR_A, 65
.equ BIGCONST, 0x89ABCDEF
.equ DERIVED, BIGCONST+8
start:
 MOV r0,#COLOR
 CMP r0,#COLOR
 ADD r1,r2,#CHAR_A
 SUB r3,r4,#CHAR_A+1
 MOV r5,r6,LSL #SHIFT
 LDR r7,[r8,#OFF]
 STR r7,[r8],#OFF
 LDRH r9,[r10,#HOFF]
 LDR r9,=BIGCONST
 LDR r11,=DERIVED
 SWI SWINUM
 SWIEQ #SWINUM
 MSR CPSR_f,#FLAGS
 .4byte COLOR,DERIVED
 .2byte COLOR
 .byte NEG+5,CHAR_A,CHAR_A-32
.equ SHIFT, 2
.equ OFF, 0x24
.equ HOFF, 0x42
.equ SWINUM, 0x123
.equ FLAGS, 0xF0000000
